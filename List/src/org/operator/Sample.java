package org.operator;

public class Sample {
	public static void main(String[] args) {
		char x = 'D';
		switch (x) {
		case 'A': {
			System.out.println("A");
			break;
		}
		case 'B': {
			System.out.println("B");
			break;
		}
		case 'C': {
			System.out.println("C");
			break;
		}
		case 'D': {
			System.out.println("D");
			break;
		}
		default:
			System.out.println("Invalid entry");
		
		}
	}

}
